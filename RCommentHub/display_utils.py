"""
RCommentHub — 表示用テキスト整形ユーティリティ

監視用・配信用で同一の整形ロジックを使い、改行位置を統一する。
"""

# ─── NBSP 置換による折り返し統一 ─────────────────────────────────────────────
#
# tkinter の tk.Label (wraplength) および canvas.create_text (width=) は
# ASCII 半角スペース U+0020 を折り返し位置として扱う。
# そのため「コード インタープリター...」のようなコメントで
# 半角スペースの直後に不自然な改行が起きる。
#
# ノーブレークスペース U+00A0 (NBSP) はどちらのウィジェットでも
# 折り返し対象にならないため、表示専用にスペースを NBSP へ置換することで
# 監視用・配信用の改行結果を統一できる。
#
# ※ 元のコメント文字列（ログ・内部データ）は変更しない。
#    この関数は **表示テキスト生成時のみ** 呼び出すこと。

_NBSP = "\u00A0"


def normalize_display_text(text: str) -> str:
    """
    表示専用テキスト整形。

    半角スペース (U+0020) を NBSP (U+00A0) に置換して
    tkinter の不自然な空白起因折り返しを防ぐ。

    Parameters
    ----------
    text : str
        元のコメント本文 (生データは変更しない)

    Returns
    -------
    str
        表示用に整形されたテキスト
    """
    if not text:
        return text
    return text.replace(" ", _NBSP)
